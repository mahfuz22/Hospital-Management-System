package bean;

public class AddStuff {
	String stuffName;
	String stuffEmail;
	String gender;
	String password;
	String stuffAddress;
	String stuffPhone;
	public String getStuffName() {
		return stuffName;
	}
	public void setStuffName(String stuffName) {
		this.stuffName = stuffName;
	}
	public String getStuffEmail() {
		return stuffEmail;
	}
	public void setStuffEmail(String stuffEmail) {
		this.stuffEmail = stuffEmail;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getStuffAddress() {
		return stuffAddress;
	}
	public void setStuffAddress(String stuffAddress) {
		this.stuffAddress = stuffAddress;
	}
	public String getStuffPhone() {
		return stuffPhone;
	}
	public void setStuffPhone(String stuffPhone) {
		this.stuffPhone = stuffPhone;
	}
}
